# print (ekrana çıktısını verir)
name = "Hikmet"
print("Name = ",name)

# type (tipini gösterir)
print("Name type is = ",type(name))

# help (komut ile ilgili yardım gösterir)
help(print)

# len (length) (karakter uzunluğunu gösterir)
print("Name length is : ",len(name))

# dir (kullanılabilecek komutları gösterir)
print(dir(name))
